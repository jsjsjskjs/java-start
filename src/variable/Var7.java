package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // true 또는 false
        char d = 'A'; // 문자 하나, 작은 따옴표를 사용해서 감싸야 한다
        String e = "Hello"; // 문자열, 큰따움표를 사용해야 한다

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // 변수의 초기화에서 개발자가 직접 적은 값들을 리터럴(literal) 이라고 한다.
        // 100, 10.5, true, 'A', "Hello" 모두 리터럴이다.
        // 변수의 값은 변할 수 있지만 리터럴은 변하지 않는 고정된 값이다.
    }
}
