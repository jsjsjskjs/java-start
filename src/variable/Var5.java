package variable;

public class Var5 {

    public static void main(String[] args) {
        // 변수 선언과 초기화를 각각 따로
        int a;
        a = 10;
        System.out.println(a);

        // 변수 선언과 초기화를 한 번에
        int b = 10;
        System.out.println(b);

        // 여러 변수 선언 및 초기화를 한 번에
        int c = 10, d = 20;
        System.out.println(c + d);
    }
}
